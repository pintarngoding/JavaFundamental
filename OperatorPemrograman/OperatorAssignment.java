public class OperatorAssignment {
    public static void main(String[] args) {
        int var = 10;
        System.out.println("var = " + var);

        var += 10;
        System.out.println("var += 10 = " + var);

        var -= 5;
        System.out.println("var -= 5 = " + var);

        var *= 2;
        System.out.println("var *= 2 = " + var);

        var /= 4;
        System.out.println("var /= 4 = " + var);

        var %= 2;
        System.out.println("var %= 2 = " + var);
    }
}
