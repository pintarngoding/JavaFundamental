public class contoh_nestedloop {
    public static void main(String[] args) {
        // nested loop number formation 12345
        for (int i = 1; i <= 10; i++) {
            for (int j = 1; j <= 10; j++) {
                System.out.print(j + " ");
            }
            System.out.println();
        }
    }
}
